'''
basic data types:
int - wole numbers ex: 5
float - fraction/decimal numbers ex: 2.345
complex - real + imaginary numbers ex: 5 + 7j
char - single letters ex: a, b, z...
str - string a set of caracters ex: Rajesh
    - here str.substring[startindex:endindex] -> [0:2] -> [Raj]

Python specifics:
list - a list of items ex: [1, 4, 5, 'and', 'you']..
     - here [start:end] -> [0:-1]
     - its ordered  
     - mutable
     - methods: append -> add as last item, pop -> remove last item, insert -> arr.insert([index]), remove -> arr.remove([index]), delete -> arr.del([index]) removes ts first occurence  

tuple - a list of items ex: (1, 4, 5, 'and', 'you')..
     - its ordered  
     - immutable

set - a list of items ex: ([1, 4, 5, 'and', 'you'])..
     - its un ordered  
     - mutable 

frozenset - a list of items ex: ([1, 4, 5, 'and', 'you'])..
     - its un ordered  
     - immutable          

dict - a list of key value sets ex: {'name': 'Rajesh', 'age': 44, 'address': '23, Lowerhutt'}.
     - un ordered  
     - mutable               

'''